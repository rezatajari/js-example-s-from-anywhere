switch (new Date().getDay()) {
    case 0: {
        alert("Sunday");
        break;
    }
    case 1: {
        alert("Monday");
        break;
    }
    case 2: {
        alert("Tuesday");
        break;
    }
    case 3: {
        alert("Wednesday");
        break;
    }
    case 4: {
        alert("Thursday");
        break;
    }
}